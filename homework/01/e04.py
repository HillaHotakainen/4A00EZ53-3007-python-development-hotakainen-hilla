# Tulosta tietokoneen näytölle teksti "Anna luku 1".
print("Anna luku 1")

# Ota käyttäjältä luku 1 vastaan ja talleta se muuttujaan luku1.
luku1 = int( input() )

# Tulosta tietokoneen näytölle teksti "Anna luku 2".
print("Anna luku 2")

# Ota käyttäjältä luku 1 vastaan ja talleta se muuttujaan luku2.
luku2 = int( input() )

# Laske luku1 * luku2 ja tallenna tulos muuttujaan summa.
summa = luku1 * luku2

# Tulosta tietokoneen näytölle teksti "Summa = "
print("Summa = ")

# Tulosta tietokoneen näytölle summa muuttujan sisältö.
print(summa)